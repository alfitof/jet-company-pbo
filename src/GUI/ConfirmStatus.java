    private void tracking_btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tracking_btnSubmitActionPerformed
        String id = tracking_tblTracking.getValueAt(tracking_tblTracking.getSelectedRow(), 0).toString();
        String status = tracking_cbUbahStatus.getSelectedItem().toString();
        //System.out.println("GUI.User.tracking_btnSubmitActionPerformed() id="+id);
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih transaksi terlebih dahulu", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                new server().updateStatus(id, status);
                JOptionPane.showMessageDialog(null, "Update sukses ", "Sukses", JOptionPane.INFORMATION_MESSAGE);

                DefaultTableModel model = (DefaultTableModel) tracking_tblTracking.getModel();
                model.setRowCount(0);
                viewTrackingTransaksi(id);

                tracking_cbUbahStatus.setVisible(false);
                tracking_btnSubmit.setVisible(false);
            } catch (Exception e) {
                System.out.println("GUI.User.tracking_btnSubmitActionPerformed()" + e);
            }
        }
    }//GEN-LAST:event_tracking_btnSubmitActionPerformed

