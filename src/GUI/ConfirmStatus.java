    public void viewTrackingTransaksi(String id) {
        try {
            server Service = new server();
            ArrayList<Transaksi> list = Service.operator_tracking(id);
            DefaultTableModel model = (DefaultTableModel) tracking_tblTracking.getModel();
            if (list.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                Object[] row = new Object[9];
                for (int i = 0; i < list.size(); i++) {
                    row[0] = list.get(i).getId_transaksi();
                    row[1] = list.get(i).getPengirim();
                    row[2] = list.get(i).getPenerima();
                    row[3] = list.get(i).getAlamat_tujuan();
                    row[4] = list.get(i).getJenis_pengiriman();
                    row[5] = list.get(i).getTotal_harga();
                    row[6] = list.get(i).getPembayaran();
                    row[7] = list.get(i).getTanggal_transaksi();
                    row[8] = list.get(i).getStatus_barang();
                    model.addRow(row);
                }
            }
        } catch (Exception e) {
            System.out.println("GUI.User.view_transaksi() " + e.toString());
        }
    }
    
